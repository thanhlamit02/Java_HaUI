    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        StringBuilder sb = new StringBuilder();

        if (txtMaDH.getText().equals("")) {
            sb.append("Mã đơn hàng không được để trống \n");
            txtMaDH.setBackground(Color.gray);
        } else {
            txtMaDH.setBackground(Color.white);
        }

        if (txtMaSP.getText().equals("")) {
            sb.append("Mã Sản Phẩm không được để trống \n");
            txtMaSP.setBackground(Color.gray);
        } else {
            txtMaSP.setBackground(Color.white);
        }

        if (txtMaNV.getText().equals("")) {
            sb.append("Mã nhân viên không được để trống \n");
            txtMaNV.setBackground(Color.gray);
        } else {
            txtMaNV.setBackground(Color.white);
        }

        if (txtSLM.getText().equals("")) {
            sb.append("Số lượng mua không được để trống \n");
            txtSLM.setBackground(Color.gray);
        } else {
            txtSLM.setBackground(Color.white);
        }

        if (txtNgay.getText().equals("")) {
            sb.append("Ngày không được để trống \n");
            txtNgay.setBackground(Color.gray);
        } else {
            txtNgay.setBackground(Color.white);
        }

        if (sb.length() > 0) {
            JOptionPane.showMessageDialog(this, sb.toString(), "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        int flag = -1;
        for (DonHang o : listDH) {
            if (o.getMaDH().equalsIgnoreCase(txtMaDH.getText())) {
                flag = 0;
                break;
            } else if (tongMaNV(txtMaNV.getText()) == listNV.size()) {
                flag = 1;
                break;
            } else if (tongMaSP(txtMaSP.getText()) == listSP.size()) {
                flag = 2;
            } else if (!dieukienSL(txtSLM.getText()) ) {
                flag = 3;
            }
        }

        if (flag == -1) {
            DonHang a = new DonHang();
            a.setMaDH(txtMaDH.getText());
            a.setMaSP(txtMaSP.getText());
            a.setMaNV(txtMaNV.getText());
            a.setSoLuongMua(Integer.parseInt(txtSLM.getText()));
            a.setNgay(txtNgay.getText());

            listDH.add(a);
            fillTable();

            try {
                FileWriter fw = new FileWriter("CSDL\\DonHang.txt", true);
                BufferedWriter bw = new BufferedWriter(fw);
                bw.write(a.toString());
                bw.newLine();
                bw.close();
                fw.close();
            } catch (IOException e) {
            }
        } else if (flag == 0) {
            JOptionPane.showMessageDialog(null, "Đã tồn tại mã đơn hàng này");
        } else if (flag == 1) {
            JOptionPane.showMessageDialog(null, "Mã nhân viên này không hợp lệ ( mã nhân viên phải trùng với các mã nhân viên đã có )");
        } else if (flag == 2) {
            JOptionPane.showMessageDialog(null, "Mã sản phẩm này không hợp lệ ( mã sản phẩm phải trùng với các mã sản phẩm đã có )");
        } else if (flag == 3) {
            JOptionPane.showMessageDialog(null, "Số lượng mua phải là số dương");
        }
        btnClearActionPerformed(evt);
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed

        StringBuilder sb = new StringBuilder();

        if (txtMaDH.getText().equals("")) {
            sb.append("Mã Đơn hàng không được để trống \n");
            txtMaDH.setBackground(Color.gray);
        } else {
            txtMaDH.setBackground(Color.white);
        }

        if (sb.length() > 0) {
            JOptionPane.showMessageDialog(this, sb.toString(), "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        int choice = JOptionPane.showConfirmDialog(this, "Bạn có muốn xoá đơn hàng không? ", "Xác Nhận", JOptionPane.YES_NO_OPTION);
        if (choice == JOptionPane.NO_OPTION) {
            return;
        }

        // neu chon khong thi tu doan nay se khong thuc hien
        for (DonHang o : listDH) {
            if (o.getMaDH().equalsIgnoreCase(txtMaDH.getText())) {
                listDH.remove(o);
                break;
            }
        }
        // ghi lai file sau khi xoa san pham
        try {
            FileWriter fw = new FileWriter("CSDL\\DonHang.txt");
            BufferedWriter bw = new BufferedWriter(fw);
            for (DonHang o : listDH) {
                bw.write(o.toString());
                bw.newLine();
            }
            bw.close();
            fw.close();
        } catch (IOException e) {
        }
        fillTable();
        btnClearActionPerformed(evt);
    }//GEN-LAST:event_btnDeleteActionPerformed

